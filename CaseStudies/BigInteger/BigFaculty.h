#pragma once

class BigFaculty
{
public:
    template <typename T>
    static T faculty(const T& n)
    {
        if (n == static_cast<T> (1))
            return static_cast<T> (1);
        else
            return n * faculty(n - static_cast<T> (1));
    }
};

